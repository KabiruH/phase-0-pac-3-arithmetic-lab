function add (x,y){
    return (x+y)
}
function subtract (x,y){
    return (x-y)
}
function multiply (x,y){
    return (x*y)
}
function divide (x,y){
    return (x/y)
}
function increment (y){
    return (y +=1)
}
function decrement (y){
    return (y -=1)
}
function makeInt (y){
    return parseInt (y, 10)
}
function preserveDecimal (y){
    return parseFloat (y)
}